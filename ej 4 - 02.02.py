edad = int(input("¿Cuál es tu edad?  "))
ingresos = float(input("¿cuáles son tus ingresos mensuales?  "))
if edad >= 16 :
    print("Por tu edad tienes que tributar")
else :
    print("Por tu edad no tienes que tributar")
if ingresos >= 1000 :
    print("Por tus ingresos tienes que tributar")
else :
    print("Por tus ingresos no tienes que tributar")